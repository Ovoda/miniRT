#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		len;
	int		i;
	char	*new_str;

	i = 0;
	if (!s || !f)
		return (0);
	len = ft_strlen(s);
	if (!(new_str = (char *)malloc(sizeof(char) * (len + 1))))
		return (0);
	while (s[i])
	{
		new_str[i] = f(i, s[i]);
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
